class Mnto < ActiveRecord::Base

belongs_to :user 
belongs_to :truck
belongs_to :division 


validates_uniqueness_of :code

validates_presence_of :division_id, :code,:user_id,:truck_id,:tipo 

  has_many   :mnto_details , :dependent => :destroy
  

  def generate_mnto_number(serie)
    if Mnto.where("cast(substring(code,1,3)  as int) = ?",serie).maximum("cast(substring(code,5,10)  as int)") == nil 
      self.code = serie.to_s.rjust(3, '0') +"-000001"
    else
    self.code = serie.to_s.rjust(3, '0')+"-"+Mnto.where("cast(substring(code,1,3)  as int) = ?",serie).maximum("cast(substring(code,5,10)  as int)").next.to_s.rjust(6, '0') 
          
    end 
    
  end


	def get_activity(tipo) 


		  if  tipo == "1"
		      modulo =   "MA"
		   end 
		   if  tipo == "2"
		      modulo =   "MB"
		   end 
		   if  tipo == "3"
		      modulo =   "MC"
   
          end 
			@detalle = 	Activity.where(extra: modulo ) 
			return @detalle 
		
	end



    def get_processed
    if(self.processed == "1")
      return "Aprobado "

    elsif (self.processed == "2")
      
      return "**Anulado **"

    elsif (self.processed == "3")

      return "-Cerrado --"  

    elsif (self.processed == "4")

      return "-Facturado --"  

    else   
      return "No Aprobado"
        
    end
  end
  
  def get_processed_short
    if(self.processed == "1")
      return "Yes"
    elsif (self.processed == "3")
       return "Yes"
    else
      return "No"
    end
  end
  
  def get_return
    if(self.return == "1")
      return "Yes"
    else
      return "No"
    end
  end
  # Process the invoice
  def process
    if(self.processed == "1" or self.processed == true)          
      self.processed="1"
      self.date_processed = Time.zone.now
      self.save
    end
  end
  def cerrar
    if(self.processed == "3" )         
      
      self.processed="3"
      self.date_processed = Time.now
      self.save
    end
  end
  
  # Process the invoice
  def anular
    if(self.processed == "2" )          
      self.processed="2"
      
      
      self.date_processed = Time.now
      self.save
    end
  end

  def processed_color
    if(self.processed == "1")
      return "green"
    else
      return "red"
    end
  end
 
def cancelar
    if(self.processed == "3" )          
      self.processed="3"
      
      
      self.date_processed = Time.now
      self.save
    end
  end
 
end
